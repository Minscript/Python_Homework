# –û–±–Ω–æ–≤–ª–µ–Ω–∏—è ID –ø–æ—Å–ª–µ –∏–∑–º–µ–Ω–µ–Ω–∏–π
def fix_id():
    person = c_form.count_contact()
    if person == 0:
        return
    lst = f_edit.file_read()
    point = 2
    lst[0] = "ID: 1\n" # –ù–∞—Å–∏–ª—å–Ω–æ –∑–∞–º–µ–Ω—è–µ–º –ø–µ—Ä–≤—ã–π ID –Ω–∞ 1 –¥–ª—è –¥–∞–ª—å–Ω–µ–π—à–µ–π —Ä–∞–±–æ—Ç—ã
    for i in range(5, len(lst)):
        if i % 5 == 0: # –ö–∞–∂–¥—ã–π 5 —ç–ª–µ–º–µ–Ω—Ç –æ–±–æ–∑–Ω–∞—á–∞–µ—Ç ID. –ú—ã –∑–∞–º–µ–Ω—è–µ–º –µ–≥–æ –Ω–∞ —É–∫–∞–∑–∞–Ω–Ω–∞–π –Ω–æ–º–µ—Ä
            lst[i] = (f"ID: {point}\n")
            point += 1
    # –ò–∑–º–µ–Ω—è–µ–º —Å–∞–º –¥–æ–∫—É–º–µ–Ω—Ç
    lst.pop(-1) # –ü—Ä–æ–≥—Ä–∞–º–º–∞ –ø–æ—á–µ–º—É-—Ç–æ –≤ –∫–æ–Ω—Ü–µ –¥–æ–±–∞–≤–ª—è–µ—Ç "\n". –ò—Å–ø—Ä–∞–≤–∏–º —ç—Ç–æ –Ω–µ–¥–æ—Ä–∞–∑—É–º–µ–Ω–∏–µ
